/**
 * Parse a TripIt date object ({date, time, utc_offset, timezone}) into a DateTimeWithTimezone.
 */
const parseTripItDateTime = (dateObj: {
  date: string;
  time: string;
  timezone: string;
  utc_offset: string;
}): DateTimeWithTimezone => {
  return {
    dateTime: new Date(
      dateObj.date + "T" + dateObj.time + dateObj.utc_offset,
    ),
    timezone: dateObj.timezone,
  };
};

/**
 * Get the ICAO ident for a flight from TripIt data.
 */
export const getIdent = (flight: FlightStatus): string => {
  let airlineCode = flight.marketing_airline_code;
  if (airlineCode in IATAToICAO) {
    airlineCode = IATAToICAO[airlineCode];
  }
  return `${airlineCode}${flight.marketing_flight_number}`;
};

/**
 * Map a TripIt FlightStatus to a DisplayFlight.
 * Status defaults to "Scheduled" / "primary" since TripIt is now reservation-only.
 */
export const mapTripItToDisplayFlight = (
  flight: FlightStatus,
): DisplayFlight => {
  const ident = getIdent(flight);

  const scheduledDepartureDateObject =
    flight.Status.ScheduledDepartureDateTime ?? flight.StartDateTime;

  const scheduledArrivalDateObject =
    flight.Status.ScheduledArrivalDateTime ?? flight.EndDateTime;

  const scheduledDeparture = parseTripItDateTime(scheduledDepartureDateObject);
  const scheduledArrival = parseTripItDateTime(scheduledArrivalDateObject);

  const estimatedDeparture = flight.Status.EstimatedDepartureDateTime
    ? parseTripItDateTime(flight.Status.EstimatedDepartureDateTime)
    : undefined;

  const estimatedArrival = flight.Status.EstimatedArrivalDateTime
    ? parseTripItDateTime(flight.Status.EstimatedArrivalDateTime)
    : undefined;

  const scheduledDateDifference = Math.abs(
    (new Date(scheduledArrivalDateObject.date).getTime() -
      new Date(scheduledDepartureDateObject.date).getTime()) /
      (1000 * 60 * 60 * 24),
  );

  const estimatedDateDifference =
    flight.Status.EstimatedDepartureDateTime &&
    flight.Status.EstimatedArrivalDateTime
      ? Math.abs(
          (new Date(flight.Status.EstimatedArrivalDateTime.date).getTime() -
            new Date(
              flight.Status.EstimatedDepartureDateTime.date,
            ).getTime()) /
            (1000 * 60 * 60 * 24),
        )
      : undefined;

  const departureDelay =
    estimatedDeparture !== undefined
      ? estimatedDeparture.dateTime.getTime() -
        scheduledDeparture.dateTime.getTime()
      : undefined;

  const arrivalDelay =
    estimatedArrival !== undefined
      ? estimatedArrival.dateTime.getTime() -
        scheduledArrival.dateTime.getTime()
      : undefined;

  return {
    ident,
    marketingAirline: flight.marketing_airline,
    marketingAirlineCode: flight.marketing_airline_code,
    marketingFlightNumber: flight.marketing_flight_number,
    startAirportCode: flight.start_airport_code,
    endAirportCode: flight.end_airport_code,
    startCityName: flight.start_city_name,
    endCityName: flight.end_city_name,
    duration: flight.duration,

    statusText: "Scheduled",
    longStatusText: "Scheduled",
    statusColor: "primary",

    scheduledDeparture,
    scheduledArrival,
    estimatedDeparture,
    estimatedArrival,

    departureTerminal: flight.Status.departure_terminal,
    departureGate: flight.Status.departure_gate,
    arrivalTerminal: flight.Status.arrival_terminal,
    arrivalGate: flight.Status.arrival_gate,
    baggageClaim: flight.Status.baggage_claim,

    departureDelay,
    arrivalDelay,

    scheduledDateDifference,
    estimatedDateDifference,

    statusSource: "tripit",
    originalTripItFlight: flight,
  };
};

/**
 * Enrich a DisplayFlight with FlightAware data.
 * Only overwrites fields when FA provides non-null values.
 */
export const enrichWithFlightAware = (
  display: DisplayFlight,
  fa: FlightAwareStatus,
): DisplayFlight => {
  // Determine status color from FA data
  let statusColor: string;
  if (fa.cancelled || fa.diverted) {
    statusColor = "danger";
  } else if (fa.arrival_delay >= 300) {
    statusColor = "danger";
  } else {
    statusColor = "success";
  }

  // Build scheduled times from FA
  const scheduledDeparture: DateTimeWithTimezone = {
    dateTime: new Date(fa.scheduled_out),
    timezone: fa.origin.timezone,
  };
  const scheduledArrival: DateTimeWithTimezone = {
    dateTime: new Date(fa.scheduled_in),
    timezone: fa.destination.timezone,
  };

  // Estimated/actual: actual → estimated → scheduled fallback
  const estimatedOutStr = fa.actual_out ?? fa.estimated_out ?? fa.scheduled_out;
  const estimatedInStr = fa.actual_in ?? fa.estimated_in ?? fa.scheduled_in;

  const estimatedDeparture: DateTimeWithTimezone = {
    dateTime: new Date(estimatedOutStr),
    timezone: fa.origin.timezone,
  };
  const estimatedArrival: DateTimeWithTimezone = {
    dateTime: new Date(estimatedInStr),
    timezone: fa.destination.timezone,
  };

  // Only show estimated times if they differ from scheduled
  const hasEstimatedDeparture =
    estimatedDeparture.dateTime.getTime() !==
    scheduledDeparture.dateTime.getTime();
  const hasEstimatedArrival =
    estimatedArrival.dateTime.getTime() !==
    scheduledArrival.dateTime.getTime();

  // Compute date differences for +1 day labels
  const scheduledDepDate = fa.scheduled_out.split("T")[0];
  const scheduledArrDate = fa.scheduled_in.split("T")[0];
  const scheduledDateDifference = Math.abs(
    (new Date(scheduledArrDate).getTime() -
      new Date(scheduledDepDate).getTime()) /
      (1000 * 60 * 60 * 24),
  );

  const estDepDate = estimatedOutStr.split("T")[0];
  const estArrDate = estimatedInStr.split("T")[0];
  const estimatedDateDifference = Math.abs(
    (new Date(estArrDate).getTime() - new Date(estDepDate).getTime()) /
      (1000 * 60 * 60 * 24),
  );

  return {
    ...display,

    statusText: fa.status,
    longStatusText: fa.status,
    statusColor,

    scheduledDeparture,
    scheduledArrival,
    estimatedDeparture: hasEstimatedDeparture ? estimatedDeparture : undefined,
    estimatedArrival: hasEstimatedArrival ? estimatedArrival : undefined,

    // Only override gate/terminal/baggage if FA provides non-null/non-empty
    departureTerminal: fa.terminal_origin || display.departureTerminal,
    departureGate: fa.gate_origin || display.departureGate,
    arrivalTerminal: fa.terminal_destination || display.arrivalTerminal,
    arrivalGate: fa.gate_destination || display.arrivalGate,
    baggageClaim: fa.baggage_claim || display.baggageClaim,

    departureDelay: fa.departure_delay ? fa.departure_delay * 1000 : undefined,
    arrivalDelay: fa.arrival_delay ? fa.arrival_delay * 1000 : undefined,

    scheduledDateDifference,
    estimatedDateDifference,

    faFlightId: fa.fa_flight_id,
    inboundFaFlightId: fa.inbound_fa_flight_id,
    registration: fa.registration,
    progressPercent: fa.progress_percent,

    statusSource: "flightaware",
  };
};
