export const colorForFaFlightStatus = (status: FlightAwareStatus): string => {
  if (status.cancelled || status.diverted) {
    return "danger";
  } else if (status.arrival_delay >= 300) {
    //300 seconds == 5 mins
    return "danger";
  } else {
    return "success";
  }
};

